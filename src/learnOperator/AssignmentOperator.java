package learnOperator;

public class AssignmentOperator {

	int num1;

	void addOperator() {
		num1 += 5;
		System.out.println("Value of num1 is " + num1);
	}

	void minusOperator() {
		num1 -= 5;
		System.out.println("Value of num1 is " + num1);
	}

	void multiOperator() {
		num1 *= 5;
		System.out.println("Value of num1 is " + num1);
	}

	void divisionOperator() {
		num1 /= 5;
		System.out.println("Value of num1 is " + num1);
	}
}
